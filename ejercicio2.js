promedioTotal(users)

function promedioTotal(users) {
  let promedioCount = 0
  let promedioSum = 0
  for (let user of users) {
    for (let favorite in user.favoritesSounds) {
      promedioSum += user.favoritesSounds[favorite].volume
      promedioCount++
    }
  }
  console.log(
    `El promedio de todos los volumenes es de ${promedioSum / promedioCount}`
  )
}
